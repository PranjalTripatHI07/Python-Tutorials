#Slicing with list => working with specific group of items is called slicing with list 

players = ["player1", "player2", "player3", "player4", "player5", "player6", "player7","player8", "player9", "player10"]
print(players[0:5])
print(players[:4])
print(players[2:])
print(players[-3:])
print(players[:-3])
print(players[0:10:2]) #the third value tells about how many items you want to skip in the list