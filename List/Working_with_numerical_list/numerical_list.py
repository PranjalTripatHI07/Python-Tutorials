#range() function => We use range() function for printing series of numbers.

#syntax => 1. range(starting number, ending number)
          #2. range(ending number)
          #3. range(starting number, ending number, skipNumber)
          
for number in range(1,21):
    print(number) 

for number in range(5):
    print(number)
    
for number in range(2, 11, 2): # here we given starting number ending number and skip number(like the number we want to skip)
    print(number)